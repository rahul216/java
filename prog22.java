// program using various String methods 

public class prog22 {
    public static void main(String[] args) {
        String x = "Rahul Bachheti";
        System.out.println("Original String: " + x);
        System.out.println("Lowercase: " + x.toLowerCase());
        System.out.println("Uppercase: "+ x.toUpperCase());
        System.out.println("Length of the string: " + x.length());
        System.out.println("character at index 3: " + x.charAt(3));
        System.out.println("Substring from 2 to 5: " + x.substring(2,5));

    }
}
